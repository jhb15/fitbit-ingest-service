package datacollection.mappings;

public class Goals {
    private long caloriesOut;
    private float distance;
    private long floors;
    private long steps;

    public Goals() {
    }

    public float getDistance() {
        return distance;
    }

    public long getFloors() {
        return floors;
    }

    public long getSteps() {
        return steps;
    }

    public long getCaloriesOut() {
        return caloriesOut;
    }
}
